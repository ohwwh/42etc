char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	len;

	i = 0;
	len = 1;
	while (*s)
	{
		len ++;
		s ++;
	}
	while (i < len)
	{
		if (*s == (char)c)
			return ((char *)s);
		s --;
		i ++;
	}
	return (0);
}
